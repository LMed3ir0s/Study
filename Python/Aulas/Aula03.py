# 1) Sem Aspas
print(1, 'Lucas Medeiros', sep=')')

# 2) Com Aspas Duplas
print(2, 'Lucas "Medeiros"', sep=')')

#3) Com Aspas Simples
print(3, "Lucas 'Medeiros'", sep=')')

# 4) Escape "\" Barra Invertida, desconsidera caracter
print(4, "Lucas \'Medeiros", sep=')')

print(4, "Lucas \"Medeiros", sep=')')

#5) r, aparecer \ (escape) usado em expressões regulares
print(5, r"Lucas \"Medeiros", sep=')')
print(5, r"Lucas \"Medeiros\"", sep=')')


